package util;

public class Employe {
    private String name;
    private int age;

    public Employe() {

    }

    public String getNom() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setNom(String nom) {
        this.name = nom;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
