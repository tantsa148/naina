package rooting;

/**
 *
 * @author itu
 */
public class VerbAction {
    private String action;
    private String verb;

    public VerbAction(String action, String verb) {
        this.action = action;
        this.verb = verb;
    }

    public String getAction() {
        return action;
    }

    public String getVerb() {
        return verb;
    }

    
}

